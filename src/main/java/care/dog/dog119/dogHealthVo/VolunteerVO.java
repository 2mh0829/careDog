package care.dog.dog119.dogHealthVo;

public class VolunteerVO {
	private int volunteerId;
	private String mem_check;
	private String name; 
	private String peopleCnt;
	private String fdate1;
	private String time;
	private String vKind;
	
	public int getVolunteerId() {
		return volunteerId;
	}
	public void setVolunteerId(int volunteerId) {
		this.volunteerId = volunteerId;
	}
	public String getMem_check() {
		return mem_check;
	}
	public void setMem_check(String mem_check) {
		this.mem_check = mem_check;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPeopleCnt() {
		return peopleCnt;
	}
	public void setPeopleCnt(String peopleCnt) {
		this.peopleCnt = peopleCnt;
	}
	public String getFdate1() {
		return fdate1;
	}
	public void setFdate1(String fdate1) {
		this.fdate1 = fdate1;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getvKind() {
		return vKind;
	}
	public void setvKind(String vKind) {
		this.vKind = vKind;
	}
}
